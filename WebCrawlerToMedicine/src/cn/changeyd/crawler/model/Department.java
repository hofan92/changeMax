package cn.changeyd.crawler.model;

public class Department {

	private int department_id;
	private String department_name;
	private String department_intro;
	private String department_address;

	public int getDepartment_id() {
		return department_id;
	}

	public void setDepartment_id(int department_id) {
		this.department_id = department_id;
	}

	public String getDepartment_name() {
		return department_name;
	}

	public void setDepartment_name(String department_name) {
		this.department_name = department_name;
	}

	public String getDepartment_intro() {
		return department_intro;
	}

	public void setDepartment_intro(String department_intro) {
		this.department_intro = department_intro;
	}

	public String getDepartment_address() {
		return department_address;
	}

	public void setDepartment_address(String department_address) {
		this.department_address = department_address;
	}

}
