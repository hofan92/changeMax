package cn.changeyd.crawler.model;

public class DiseaseOrPa {
	private int disease_id;
	private String disease_name;
	private String disease_trans;
	private String organ_name;
	private String part_name;

	public int getDisease_id() {
		return disease_id;
	}

	public void setDisease_id(int disease_id) {
		this.disease_id = disease_id;
	}

	public String getDisease_name() {
		return disease_name;
	}

	public void setDisease_name(String disease_name) {
		this.disease_name = disease_name;
	}

	public String getDisease_trans() {
		return disease_trans;
	}

	public void setDisease_trans(String disease_trans) {
		this.disease_trans = disease_trans;
	}

	public String getOrgan_name() {
		return organ_name;
	}

	public void setOrgan_name(String organ_name) {
		this.organ_name = organ_name;
	}

	public String getPart_name() {
		return part_name;
	}

	public void setPart_name(String part_name) {
		this.part_name = part_name;
	}

}
